package Model;

public class BusinessAuthInfo {
	private String id;
	private String password;
	
	
	
	
	public BusinessAuthInfo(String id, String password) {
		super();
		this.id = id;
		this.password = password;
	}
	
	public String getId() {
		return id;
	} 
	public String getPassword() {
		return password;
	} 
	
	
	
}
