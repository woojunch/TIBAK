package Model;

public class ConDTO {
	private String num;
	private String name;
	private int showTime;
	private String managementInform;
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getShowTime() {
		return showTime;
	}
	public void setShowTime(int showTime) {
		this.showTime = showTime;
	}
	public String getManagementInform() {
		return managementInform;
	}
	public void setManagementInform(String managementInform) {
		this.managementInform = managementInform;
	}
	
	
}
