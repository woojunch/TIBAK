package Command;

public class ConHallCommand {
	private String conHallName;
	private int capacity;
	private String conHallAddr;
	private int row;
	private int column;
	
	public String getConHallName() {
		return conHallName;
	}
	public void setConHallName(String conHallName) {
		this.conHallName = conHallName;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public String getConHallAddr() {
		return conHallAddr;
	}
	public void setConHallAddr(String conHallAddr) {
		this.conHallAddr = conHallAddr;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getColumn() {
		return column;
	}
	public void setColumn(int column) {
		this.column = column;
	}
}
